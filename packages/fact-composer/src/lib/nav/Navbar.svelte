<script lang="ts">
  import {Button} from "$lib/components/ui/button";
  import {activeRoute, setActiveRoute} from "$lib/router/router";
  import {
    PencilRulerIcon,
    HomeIcon,
    NotebookIcon,
    TagIcon,
    ShoppingBagIcon,
    WrenchIcon,
    UserCircleIcon,
  } from "lucide-svelte";

  const tabs = [
    {
      name: "composition",
      icon: PencilRulerIcon,
    },
    {
      name: "collections",
      icon: NotebookIcon,
    },
    {
      name: "labels",
      icon: TagIcon,
    },
    {
      name: "marketplace",
      icon: ShoppingBagIcon,
    },
  ] as const;

  const bottomTabs = [
    {
      name: "settings",
      icon: WrenchIcon,
    },
    {
      name: "account",
      icon: UserCircleIcon,
    }
  ] as const;
</script>

<div class="flex flex-col min-w-14 h-full shadow items-center py-2 gap-4">
  <Button size="icon" variant="ghost">
    <HomeIcon />
  </Button>
  {#each tabs as { name, icon }}
    <Button
      size="icon"
      variant={$activeRoute === name ? "default" : "ghost"}
