    >
      <svelte:component this={icon} />
    </Button>
  {/each}
  <div class="grow"></div>
  {#each bottomTabs as { name, icon }}
    <Button
      size="icon"
      variant={$activeRoute === name ? "default" : "ghost"}
      on:click={() => setActiveRoute(name)}
    >
      <svelte:component this={icon} />
    </Button>
  {/each}
</div>
